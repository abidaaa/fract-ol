int		ft_max(int *tab, int length)
{
	int	max;
	int	i;

	if (length < 0)
		return (0);
	max = tab[0];
	i = 0;
	while (i < length)
	{
		if (tab[i] > max)
			max = tab[i];
		i++;
	}
	return (max);
}
