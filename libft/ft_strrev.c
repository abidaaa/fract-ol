char	*ft_strrev(char *str)
{
	int i;
	int j;
	int temp;

	i = 0;
	while (str[i] != '\0')
	{
		i++;
	}
	i--;
	j = 0;
	while (j < i)
	{
		temp = str[i];
		str[i] = str[j];
		str[j] = temp;
		i--;
		j++;
	}
	return (str);
}
