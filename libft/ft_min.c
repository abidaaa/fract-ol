int		ft_min(int *tab, int length)
{
	int	min;
	int	i;

	if (length < 0)
		return (0);
	min = tab[0];
	i = 0;
	while (i < length)
	{
		if (tab[i] < min)
			min = tab[i];
		i++;
	}
	return (min);
}
